    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        if(txtUser.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter username.");
            txtUser.setText("");
            txtUser.grabFocus();
        }
        else if(txtPass.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter password.");
            txtPass.setText("");
            txtPass.grabFocus();
        }
        else
        {
            try {
                String username = txtUser.getText();
                String password = txtPass.getText();
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/student","root","");
                pst = con.prepareStatement("select * from registration where username=? and password=?");
                pst.setString(1, username);
                pst.setString(2, password);
                rs = pst.executeQuery();
                if(rs.next())
                {
                    StudentDBMysql m = new StudentDBMysql();
                    this.hide();
                    m.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Username or password do not match.");
                    txtUser.setText("");
                    txtPass.setText("");
                    txtUser.requestFocus();
                }

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Login_1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

